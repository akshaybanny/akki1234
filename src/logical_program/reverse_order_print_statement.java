package logical_program;

public class reverse_order_print_statement {
	public static void main(String[] args) {
		
		String s1="my name is rahul";
		String []ar=s1.split(" ");
		
		
		for(int i=ar.length-1;i>=0;i--) {
			System.out.println(ar[i]);
		}
		
		
		
		
		
		
		
		
		
	}
	

}
